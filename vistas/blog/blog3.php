        <h1>¿Conoces el potencial de cada red social?</h1>
        <div class="imagen">
            <img src="../../src/images/potencial_social.webp" alt="social media PYMES">
        </div>
        <div class="texto">
            <p>Las redes sociales revolucionaron el mercado. ¿Alguna vez ha investigado una marca en los medios antes de realizar una compra, o alguna vez el número de comentarios positivos de una publicación te generó confianza acerca de una marca? Es probable que sí.
            Por ello te brindaremos información sobre el potencial de cada red social:
            </p>
            <h2>FACEBOOK:</h2>
            <p>La red más popular de la actualidad, Facebook permite explorar diversos formatos en su plataforma, sobre todo imágenes y videos. Al consumidor de hoy en día busca páginas que cubran sus necesidades y les llame la atención, hoy en día ello se volvió la prioridad número uno de distintas marcas.</p>
            <br>
            <p>Utiliza el lado humano de tu marca, grabando videos o posteando fotos constantemente, con un toque diferente en cada uno, hace que poco a poco tu marca este posicionada.</p>
            <br>
            <p>Las imágenes son muy eficientes, especialmente si conoces bien a tu audiencia. Úsalas con frases y elementos que generen identificación y, siempre que posible, introduce una oración interrogativa en la descripción para aumentar la posibilidad de interacción.</p>
            <br>
            <p>Aquí los memes pueden funcionar muy bien, pero ten siempre en mente quién es tu público y de qué forma el meme puede involucrarse con tu marca.</p>
            <h2>INSTAGRAM:</h2>
            <p>La red que crece a un ritmo acelerado, Instagram ha dejado bastante claro que el contenido visual es el gran protagonista de la plataforma. Todos sus recursos están orientados a la divulgación de imágenes y videos, así que no habría otra forma de destacarse por aquí.</p>
            <br>
            <p>El uso de los hashtags que estén relacionados a tu marca puede funcionar muy bien para poder posicionarte, el feed de la página también es un factor fundamental. Recuerda siempre usar imágenes propias, y si en caso comparte alguna que no es tuya, debes dar los créditos correspondientes, ya que, eso dice mucho de tu ética como marca.</p>
            <br>
            <p>Los recursos stories, IGTV y reels son excelentes para transmitir contenidos más extensos como los videos. Muchas marcas los utilizan para compartir el backstage, el día a día de la empresa, eventos, entrevistas, y más.</p>
            <h2>TWITTER:</h2>
            <p>Twitter siempre fue un escenario para la libertad de expresión y, no en vano, se convirtió en la plataforma de grandes debates políticos y sociales. Esto nos lleva a creer que su uso suele ser mucho más personal, sin embargo, si bien trabajada, las empresas también pueden aprovechar sus ventajas.</p>
            <br>
            <p>La red tiene un perfil bastante informativo debido a sus características como el límite de caracteres, los trending topics y la forma como "corren" los tweets en la plataforma. Por ese motivo, las personas la utilizan para mantenerse actualizadas.</p>
            <br>
            <p>Puedes usar la plataforma de manera estratégica para que tu producto o servicio pueda hacerse viral. Los memes y el humor también son bien recibidos por los usuarios de la red, así que mantente al tanto para no perder la oportunidad de utilizarlos (mientras estén de acuerdo con el contexto de tu marca). Los hashtags son importantes, no lo olvides.</p>
            <h2>LINKEDIN:</h2>
            <p>Es una red profesional en la cual los usuarios buscan aumentar su red de contactos empresariales y oportunidades de negocio. Para las marcas, la red sirve principalmente para fortalecer el interés de las personas en trabajar en la empresa.</p>
            <br>
            <p>Los artículos escritos tienen un resultado bastante positivo, por lo tanto, invierte en la escritura y en el intercambio de contenidos con tu audiencia. Agregar una imagen relacionada con el contexto de la publicación puede volverla más atractiva y captar aún más la atención de los usuarios. No hay excusa para no poder posicionar tu marca en esta plataforma.</p>
            <br>
            <p>Por último, Digimedia cuenta con el servicio de Community Manager quién es el responsable de comunicarse con tu público objetivo en redes sociales para crear presencia de marca. Además de atraer audiencia a través de diferentes tácticas y estrategias digitales y se mantiene actualizado a tendencias que respondan a las necesidades e intereses de los usuarios.</p>
